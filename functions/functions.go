package functions

func Add(a int, b int) int {
    return a + b
}

func AddMany(a int, b int, c int) int {
    return a + b + c
}

func Minus(a int, b int) int {
    return a - b
}

func Multiply(a int, b int) int {
    return a * b
}

func Divide(a int, b int) int {
    return a / b
}

func IsEven(a int) bool {
    return a % 2 == 0
}

